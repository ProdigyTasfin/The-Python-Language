a  = 32
s = 3.1416
d = True
f = 'Tasfin'

b = type(a)
t = type(s)
e = type(d)
g = type(f)

print(b)  # <class 'int'>
print(t)  # <class 'float'>
print(e)  # <class 'bool'>
print(g)  # <class 'str'>

# type casting

print(float(a))  # <class 'int'>
print(int(s))  # <class 'float'>
print(str(d))  # <class 'bool'>
print(bool(f))  # <class 'str'>


